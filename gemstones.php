<h2><font color="#e3c676"><center>Types of gemstones</center></font></h2>
<h4>
<br><br>
Of the more than 2,000 identified natural minerals, fewer than 100 are used as gemstones and only 16 have achieved importance. These are beryl, chrysoberyl, 
corundum, diamond, feldspar, garnet, jade, lazurite, olivine, opal, quartz, spinel, topaz, tourmaline, turquoise, and zircon. Some of these minerals provide 
more than one type of gem; beryl, for example, provides emeralds and aquamarines, while corundum provides rubies and sapphires. In virtually all cases, the 
minerals have to be cut and polished for use in jewelry.
Except for diamond, which presents special problems because of its very great hardness (see diamond cutting), gemstones are cut and polished in any of three ways. 
Agate, opal, jasper, onyx, chalcedony (all with a Mohs hardness of 7 or less) may be tumbled; that is, they may be placed in a cylinder with abrasive grit and water 
and the cylinder rotated about its long axis. The stones become polished but are irregular in shape. Second, the same kinds of gemstones may instead be cut en cabochon 
(i.e., with a rounded upper surface and a flat underside) and polished on water- or motor-driven sandstone wheels. Third, gemstones with Mohs hardness of more than 7 
may be cut with a carborundum saw and then mounted in a holder (dop) and pressed against a lathe that can be made to revolve with extreme rapidity. The lathe carries a 
point or small disk of soft iron, which can vary in diameter from that of a pinhead to a quarter of an inch. The face of the disk is charged with carborundum grit, 
diamond dust, or other abrasives, along with oil.<br>

