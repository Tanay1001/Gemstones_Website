<!--****WHAT ARE GEMSTONES****-->
<h2><font color="#e3c676"><center>What are gemstones?</center></font></h2>

<h4>
A gemstone (also called a gem, fine gem, jewel, precious stone, or semi-precious stone) is 
a piece of mineral crystal, which, in cut and polished form, is used to make jewellery or other 
adornments. However, certain rocks (such as lapis lazuli and opal) and occasionally organic 
materials that are not minerals (such as amber, jet, and pearl) are also used for jewellery 
and are therefore often considered gemstones as well. Most gemstones are hard, but some soft 
minerals are used in jewellery because of their lustre or other physical properties that have 
aesthetic value. Rarity is another characteristic that lends value to a gemstone.
</h4>
