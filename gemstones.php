<h2><font color="#e3c676"><center>How are they formed?</center></font></h2>
<h4>
There are five requirements for crystals to form: ingredients and temperature, pressure, time, and space. 
To better explain the basics of mineral crystallisation lets talk about rock candy for a second. Rock candy is simply crystallised sugar.
If you stir as much sugar as possible into a bottom of water you will see it begin to settle at the bottom of the pot. 
When no more sugar can dissolve, you have reached saturation point. The water can't absorb any more sugar, this is known as super-saturated
This is a great way to understand just how gemstones form.
Now bring the pot to a boil- the saturation level changes at boiling point. You can add more sugar, you should do so until you reach super-saturation.
 At this stage, you should remove the pot from heat. As the water returns to room temperature the sugar it can hold returns to its previous level. 
The excess sugar comes out of its solution and crystallises as it does so.
Now, hang a string in the solution so the crystals have something to grow on. Ideally, weight the string to keep it straight. Once the water has 
totally cooled the string will be covered in crystals.
Gemstone Formation Process
Generally speaking there are 4 ways gemstones can form. They are<br>
Igneous- These minerals are created deep within the earth (Diamonds, Ruby, Sapphire, Peridot)<br>
Hydrothermal- Similar to the rock candy example, gemstones are formed when bodies of mineral rich water cool<br>
Metamorphic- As the name suggests, these are gems that are 'morphed' due to intense heat and pressure. (Sapphire, Ruby, Spinel, Garnet)<br>
Sedimentary- Gems that form due to water depositing sediments (Malachite, Azurite, Opal)<br>
