<h2><font color="#e3c676"><center>What is GIA?</center></font></h2>

<h4>
As mentioned above, a GIA diamond is really shorthand diamond sellers use to describe diamonds that have been graded by GIA. GIA is an independent, 
nonprofit organization that conducts gem research, educates gem professionals and sets the standards for determining diamond quality. One of GIA's main functions 
is the grading of diamonds.
