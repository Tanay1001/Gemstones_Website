<!--****WHAT IS GEMOLOGY****-->
<h2><font color="#e3c676"><center>What is gemology?</center></font></h2>


<h4>
Gemology or gemmology is the science dealing with natural and artificial gemstone materials. 
It is a geoscience and a branch of mineralogy. Some jewelers (and many non-jewelers) are academically 
trained gemologists and are qualified to identify and evaluate gems.Special Gemology deals with categories 
and varieties of gemstones, synthetic stones and imitations. Conventionally, the organic substance such as 
amber, pearls, coral and non-minerals are also included in this subject of special gemology.
Practical Gemology refers to the application of the knowledge of characteristics and properties of gemstones 
in identification or separation of synthetics and imitations. Diamond grading is included in practical gemology.
