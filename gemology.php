<h2><font color="#e3c676"><center>What is GCI?</center></font></h2>

<h4>
The GCI Gemological Laboratories & College is a certified universal institute providing gemological services, 
such as diamond grading reports (included fancy color), enhanced diamonds identification (HPHT, irradiations), man -made 
/ synthetic diamonds and imitation detection as well as certification of diamonds, gemstones and jewelry. Exhaustive professional 
courses on rough and polished diamonds, gemstones and jewelry design, given by The College for Diamonds Gemstones and Jewelry, will enable 
you to acquire essential knowledge, as well as a vast practical experience. Graduating in the polished diamond course will grant you a D.G. 
(Diamond Grader) diploma, while passing the exams of all three courses will appoint you as a Graduate Gemologist (G.G. diploma).<br>
