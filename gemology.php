<h2><font color="#e3c676"><center>Gemology Careers</center></font></h2>

<h4>
A Gemologist identifies the gems and advises the jewellers about the compatibility of gems to particular metals. Mostly, this profession is adopted only by those 
whose ancestors have been into this profession from centuries ever since the time of emperors and kings and before. Gemstones are still considered to be the most 
precious things in India. The knowledge of gems and people's ability to buy it has increased among the masses in huge numbers and thus the demand of Gemstones and 
the professionals of this field have increased a lot in the last 50 years or so.
Also, considering the fact that India is the largest consumer of gold in the world and has excellent infrastructure for diamond cutting, the career of a 
Gemologist has emerged as very attractive and satisfying field. A gemologist can start their career by working with jewellery houses or can work as a freelancer.
