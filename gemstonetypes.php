<br>
SAPPHIRE
<p>
The name "sapphire" can also apply to any corundum that's not ruby red, another corundum variety.<br>
Besides blue sapphire and ruby, the corundum family also includes so-called "fancy sapphires." They come 
in violet, green, yellow, orange, pink, purple, and intermediate hues. Some stones exhibit the phenomenon 
known as color change, most often going from blue in daylight or fluorescent lighting to purple under incandescent 
light. Sapphires can even be gray, black, or brown.<br><br>
Mineral: Corundum<br>
Chemistry: Al2O3<br>
Color: Every color but red<br>
Refractive index: 1.762 to 1.770<br>
Birefringence: 0.008 to 0.010<br>
Specific gravity: 4.00<br>
Mohs Hardness: 9<br>
</p>
