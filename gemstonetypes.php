AMETHYST
<p>
The essence of the color purple, amethyst is beautiful enough for crown jewels yet affordable enough for class rings.<br>
Amethyst was as expensive as ruby and emerald until the 19th Century, when Brazil's large deposits were discovered. It was 
believed to prevent intoxication-amethystos means "not drunk" in ancient Greek. Today, as the most valued quartz variety, 
amethyst is in demand for designer pieces and mass-market jewelry alike, and its purple to pastel hues retain wide consumer appeal.<br><br>
Mineral: Quartz<br>
Chemistry: SiO2<br>
Color: Purple<br>
Refractive Index: 1.544 to 1.553<br>
Birefringence: 0.009<br>
Specific Gravity: 2.66<br>
Mohs Hardness: 7
</p>
