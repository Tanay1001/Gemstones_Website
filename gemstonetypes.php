<br>
TOPAZ
<p>
Honey yellow. Fiery orange. Cyclamen pink. Icy blue. In warm or cool tones, topaz is a lustrous and brilliant gem.<br>
Topaz actually has an exceptionally wide color range that, besides brown, includes various tones and saturations of blue, green, 
yellow, orange, red, pink, and purple. Colorless topaz is plentiful, and is often treated to give it a blue color. Topaz is also 
pleochroic, meaning that the gem can show different colors in different crystal directions.<br><br>
Mineral: Topaz<br>
Chemistry: Al2(F,OH)2SiO4<br>
Color: Yellow, orange, brown, pink to red to purple red, blue, light green and colorless<br>
Refractive index: 1.619 to 1.627<br>
Birefringence: 0.008 to 0.010<br>
Specific gravity: 3.53<br>
Mohs Hardness: 8<br>
</p>
