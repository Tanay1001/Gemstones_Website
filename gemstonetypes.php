<br>
JADE
<p>
Jade is actually two separate gems: nephrite and jadeite. In China, a pierced jade disk is a symbol of heaven.<br>
Jade has its cultural roots in the smoke-dimmed caves and huts that sheltered prehistoric humans. Around the world, 
Stone Age workers shaped this toughest of gems into weapons, tools, ornaments, and ritual objects. Their carvings invoked 
the powers of heaven and earth and mystic forces of life and death.<br><br>
Mineral: Jadeite and Nephrite<br>
Chemistry: NaAlSi2O6 and Ca2(Mg,Fe)5Si8O22(OH)2<br>
Color:  Green, white, orange, yellow, lavender, black<br>
Refractive index: 1.666 to 1.680 (+/-0.008) and 1.606 to 1.632 (+0.009, 0.006)<br>
Birefringence:  Usually not detectable<br>
Specific gravity: 3.34 and 2.95<br>
Mohs Hardness: 6.5 to 7 and 6.0 to 6.5<br>
</p>
