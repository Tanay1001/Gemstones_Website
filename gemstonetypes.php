<br>
AMBER
<p>
Amber is nature's time capsule. This fossilized tree resin contains remnants of life on earth millions of years ago.<br>
The Greeks called amber elektron, or "made by the sun." Homer praised its bright glow. The Egyptians buried it in tombs for 
the afterlife. Today's scientists value amber too: it provides a three-dimensional window into prehistoric ecosystems through the myriad 
animal and plant inclusions it contains.<br><br>
Organic, not mineral: Fossilized resin<br>
Chemistry: C10H16O<br>
Color: Yellow, orange, and brown<br>
Refractive index: 1.540<br>
Specific gravity: 1.08<br>
Mohs Hardness: 2.0 to 2.5<br>
</p>
