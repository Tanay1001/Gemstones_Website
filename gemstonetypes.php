<br>
TURQUOISE
<p>
Azure sky, robin's egg blue: Vivid shades of turquoise define the color that's named after this gem.<br>
Turquoise is found in only a few places on earth: dry and barren regions where acidic, copper-rich groundwater 
seeps downward and reacts with minerals that contain phosphorus and aluminum. The result of this sedimentary 
process is a porous, semitranslucent to opaque compound of hydrated copper and aluminum phosphate.<br><br>
Mineral: Turquoise<br>
Chemistry: CuAl6(PO4)4  (OH)8.5H2O<br>
Color: Blue to green<br>
Refractive Index: 1.610 to 1.650<br>
Birefringence: Not detectable<br>
Specific Gravity: 2.76 (+0.14, -0.36)<br>
Mohs Hardness: 5 to 6<br>
</p>
