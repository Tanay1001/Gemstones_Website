EMERALD
<p>
Emerald is the bluish green to green variety of beryl, a mineral species that includes aquamarine<br>
Gem experts differ on the degree of green that makes one stone an emerald and another stone a less-expensive green beryl. Most gemologists, 
gemological laboratories, and colored stone dealers call a stone green beryl when its color is "too light" for it to be classified as emerald. 
Even among that group, however, there's a difference of opinion about what's considered "too light."<br><br>
Mineral: Beryl<br>
Chemistry: Be3Al2Si6O18<br>
Color: Vibrant green<br>
Refractive Index: 1.577 to 1.583<br>
Birefringence: 0.005 to 0.009<br>
Specific Gravity:   2.72<br>
Mohs Hardness: 7.5 to 8<br>
</p>
