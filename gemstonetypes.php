<br>
ROSE QUARTZ
<p>
Rose quartz is a quartz variety that gets its name from its delicate pink color.<br>
The color of rose quartz ranges from very light pink (almost white) to medium-dark pink. The most appealing color typically occurs in 
larger sizes; small rose quartz specimens with good color tend to be scarce. Rose quartz is usually found in massive form, so well-shaped 
crystals are highly prized.<br><br>
Mineral: Quartz<br>
Chemical composition: SiO2<br>
Color: Very light pink to medium-dark pink<br>
Refractive index: 1.544 to 1.553<br>
Specific gravity: 2.66 (+0.03/-0.02)<br>
Mohs hardness: 7<br>
</p>
