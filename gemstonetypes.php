<br>
PEARL
<p>
Perfect shining spheres. Lustrous baroque forms. Seductive strands, warm to the touch. Pearls are simply and purely organic.<br>
Perhaps the best-loved gems of all time, pearls-natural and cultured-occur in a wide variety of colors. The most familiar are 
white and cream, but the palette of colors extends to every hue. Natural pearls form around a microscopic irritant in the bodies 
of certain mollusks. Cultured pearls are the result of the deliberate insertion of a bead or piece of tissue that the mollusk coats with nacre.<br><br>
Mineral: Calcium Carbonate<br>
Chemistry: CaCO3<br>
Color: White, black, gray, yellow, orange, pink, lavender, green, blue<br>
Refractive Index: 1.52-1.69<br>
Specific Gravity: 2.60-2.85<br>
Mohs Hardness: 2.5-3.0<br>
</p>
