<img src="cut.jpg" width=100%>
<h4><font color="#e3c676">
Diamonds are renowned for their ability to transmit light and sparkle so intensely. We often think of a diamond's cut as shape but what 
diamond cut actually does mean how well a diamond's facets interact with light.
Achieving the best cut for a diamond reflects in the stone's final beauty and value. And of all the diamond 4Cs, it is the most complex and technically difficult to analyze. 
To determine the cut grade of the standard round brilliant diamond - the shape that dominates the majority of diamond jewelry. These proportions allow to evaluate what the best cut 
for a diamond is, by studying how successfully a diamond interacts 
with light to create desirable visual effects, such as:
Brightness: Internal and external white light reflected from a diamond
Fire: The scattering of white light into all the colors of the rainbow
Scintillation: The amount of sparkle a diamond produces, and the pattern of light and dark areas caused by reflections within the diamond
