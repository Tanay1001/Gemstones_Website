<!--****WHAT ARE DIAMONDS****-->
<h2><font color="#e3c676"><center>What are diamonds?</center></font></h2>


<h4>
Diamond is a rare, naturally occurring mineral composed of carbon. 
Each carbon atom in a diamond is surrounded by four other carbon atoms 
and connected to them by strong covalent bonds - the strongest type of 
chemical bond. This simple, uniform, tightly-bonded arrangement yields one 
of the most durable and versatile substances known.Diamond also has special 
optical properties such as a high index of refraction, high dispersion, and 
high luster. These properties help make diamond the world's most popular gemstone 
and enable it to be used in specialty lenses where durability and performance 
are required.
<snap id="howaretheyformed"></snap>
</h4>


<!--****HOW ARE THEY FORMED****-->
<br><br>
<h2><font color="#e3c676"><center>How are they formed?</center></font></h2>

<h4>
Geologists believe that the diamonds in all of Earth's commercial diamond deposits 
were formed in the mantle and delivered to the surface by deep-source volcanic eruptions. 
These eruptions produce the kimberlite and lamproite pipes that are sought after by 
diamond prospectors.
<br><br>
Tiny diamonds have been found in rocks that are thought to have been subducted deep into 
the mantle by plate tectonic processes - then returned to the surface Diamond formation in 
a subducting plate might occur as little as 50 miles (80 kilometers) below the surface and 
at temperatures as low as 390 degrees Fahrenheit (200 degrees Centigrade) 
<center><img src="gif1.gif"></center>
Throughout its history, Earth has been repeatedly hit by large asteroids. When these asteroids 
strike the earth, extreme temperatures and pressures are produced. For example: when a six mile
(10 kilometer) wide asteroid strikes the earth, it can be traveling at up to 9 to 12 miles per 
second (15 to 20 kilometers per second). Upon impact this hypervelocity object would produce an 
energy burst equivalent to many nuclear weapons and temperatures hotter than the sun's surface.
The high temperature and pressure conditions of such an impact are more than adequate to form diamonds.
