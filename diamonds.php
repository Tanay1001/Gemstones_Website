<h2><font color="#e3c676"><center>4c's in diamonds?</center></font></h2>

<div id="div1" align="center">
<h1>Carat<h1>
<img src="carat.jpg" width=100%>
<h4>
To put it simply, diamond carat weight measures how much a diamond weighs. 
A metric "carat" is defined as 200 milligrams. Each carat is subdivided into 100 'points.' This allows 
very precise measurements to the hundredth decimal place. A jeweler may describe the weight of a diamond 
below one carat by its 'points' alone. For instance, the jeweler may refer to a diamond that weighs 0.25 
carats as a 'twenty-five pointer.' Diamond weights greater than one carat are expressed in carats and decimals. 
A 1.08 carat stone would be described as 'one point oh eight carats.'
All else being equal, diamond price increases with diamond carat weight because larger diamonds are rarer and more 
desirable. However, two diamonds of equal carat weight can have very different values (and prices) depending on three 
other factors of the diamond 4Cs: Color, Clarity, and Cut.
While now you know what carat means, it's also important to remember that a diamond's value is determined using all of 
the 4Cs, and not just carat weight.
<br><br>
</h4>
</div>

<div id="div2" align="center">
<h1><font color="#e3c676">Color</font><h1>
<img src="color.jpg" width=100%>
<h4><font color="#e3c676">
Understanding what diamond color means helps in choosing the right diamond. Interestingly, the diamond color evaluation of 
most gem-quality diamonds is based on the absence of color. A chemically pure and structurally perfect diamond has no hue, 
like a drop of pure water, and consequently, a higher value. GIA's D-to-Z diamond color-grading system measures the degree 
of colorlessness by comparing a stone under controlled lighting and precise viewing conditions to masterstones of established color value.
Many of these diamond color distinctions are so subtle that they are invisible to the untrained eye; however, these distinctions make 
a very big difference in diamond quality and price. Hence, it is important to get a GIA expert's opinion in evaluating the best color for your diamond.
<br><br><br><br><br><br><br><br><br><br>
</font></h4>
</div>

<div id="div3" align="center">
<h1>Clarity<h1>
<img src="clarity.jpg" width=100%>
<h4>
To understand diamond clarity, we must first understand how diamonds are created. Natural diamonds are the result of carbon exposed to tremendous heat and 
pressure deep in the earth. This process can result in a variety of internal characteristics called 'inclusions' and external characteristics called 'blemishes.'
Evaluating diamond clarity involves determining the number, size, relief, nature, and position of these characteristics, as well as how these affect the overall 
appearance of the stone. If you are trying to determine what is the best clarity for a diamond, remember that no diamond is perfectly pure. But the closer it comes 
to purity, the better its clarity.To the naked eye, diamond's may look exactly the same, 
but these diamonds are quite different in terms of overall quality. This is why expert and accurate assessment of diamond clarity is extremely important. Knowing what diamond 
clarity truly means helps you understand the factors that contribute to diamond quality and price.
<br><br>
</h4>
</div>

